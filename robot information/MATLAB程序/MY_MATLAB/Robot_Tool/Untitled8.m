% v1 = [x1, y1];  % 请用实际的向量v1坐标替换这些值
% v2 = [x2, y2];  % 请用实际的向量v2坐标替换这些值

v2 = Robot_Y2(4:5,5);  % 请用实际的向量v1坐标替换这些值
v1 = Robot_Y2(4:5,6);  % 请用实际的向量v2坐标替换这些值
x1 = v1(1);
y1 = v1(2);
x2 = v2(1);
y2 = v2(2);
% 计算 "叉乘" 结果
crossProduct = x1 * y2 - x2 * y1;

% 判断 "叉乘" 结果的正负，来确定旋转方向的正负
if crossProduct > 0
    disp('顺时针旋转');  % 正值表示顺时针旋转
elseif crossProduct < 0
    disp('逆时针旋转');  % 负值表示逆时针旋转
else
    disp('两个向量共线');  % 零值表示两个向量共线
end